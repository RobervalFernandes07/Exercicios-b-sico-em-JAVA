package Exercicios2;

public class Ex21 {
    //Escreva um algoritmo que exiba 20 vezes a mensagem "Eu gosto de estudar algoritmos" , utilizando Do..while.
    public static void main(String[] args){

        int x ;

        x = 1;
        do{
            System.out.println("Eu gosto de estudar algoritimos");

            x = x + 1;
        }while (x <=20);
    }
}
